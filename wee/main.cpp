#include <iostream>

using namespace std;

int print(int n){
    int y,i,k=1,g=1;
    if (n==1){
        y==1;
    }
    else {
        for (i=1;i<=n;i++){
            g*=k;
            k++;
        }
        return y+=g+1;
    }
    cout<<y;
}
int main()
{
    print(3);
    return 0;
}
